import numpy as np
from scipy.signal import iircomb, filtfilt
from typing import Union

def iircombfilter(
    signal: np.ndarray, 
    sample_rate: float, 
    wfreq:float=50,
    quality_factor: float = 50.0
) -> np.ndarray:
    """
    Удаляет сетевую помеху 50 Гц из сигнала с помощью IIR гребенчатого фильтра.

    Эта функция создает режекторный фильтр (bandstop), который подавляет частоту 50 Гц
    и её гармоники, используя алгоритм IIR Comb Filter. Для применения фильтра 
    используется метод sosfiltfilt, обеспечивающий нулевой фазовый сдвиг.

    Параметры
    ---------
    signal : np.ndarray
        Входной одномерный массив данных (сигнал), из которого нужно удалить шум.
    sample_rate : float
        Частота дискретизации сигнала в Герцах (Гц). 
        Например, 1000 для 1 кГц или 250 для 250 Гц.
    quality_factor : float, optional
        Добротность фильтра (Q-фактор). Определяет ширину полосы подавления.
        По умолчанию 50.0.
        - Более высокое значение (напр., 50-100) вырезает очень узкую полосу вокруг 50 Гц.
        - Более низкое значение (напр., 10-20) вырезает более широкую полосу, 
          что полезно, если частота сети "плавает" (например, 49-51 Гц).

    Возвращает
    ----------
    np.ndarray
        Массив сигнала той же длины, что и входной, но без компоненты 50 Гц.

    Пример
    ------
    >>> import numpy as np
    >>> fs = 1000  # Частота дискретизации 1000 Гц
    >>> t = np.linspace(0, 1, fs)
    >>> # Создаем тестовый сигнал: полезный сигнал 10 Гц + шум 50 Гц
    >>> clean_signal = np.sin(2 * np.pi * 10 * t)
    >>> noise = 0.5 * np.sin(2 * np.pi * 50 * t)
    >>> dirty_signal = clean_signal + noise
    >>> 
    >>> # Очищаем сигнал
    >>> filtered_signal = remove_50hz_noise(dirty_signal, fs)
    """
    
    # 1. Нормализация частоты
    # Функция iircomb ожидает частоту в долях от частоты Найквиста (0..1).
    # Частота Найквиста = sample_rate / 2
    nyquist = sample_rate / 2.0
    w0 =  wfreq/ nyquist

    # Проверка на валидность частоты (частота Найквиста должна быть > 50 Гц)
    if w0 >= 1.0:
        raise ValueError(
            f"Частота дискретизации ({sample_rate} Гц) слишком низка. "
            "Она должна быть больше 100 Гц для фильтрации 50 Гц (теорема Котельникова)."
        )

    # 2. Создание коэффициентов фильтра
    # ftype='bandstop' означает, что мы хотим УДАЛИТЬ эту частоту (режекторный фильтр).
  
    b,a = iircomb(
        w0, 
        Q=quality_factor, 
        ftype='notch', 
       
    )

    # 3. Применение фильтра
    # filtfilt применяет фильтр дважды (вперед и назад), что устраняет фазовые искажения.
    filtered_signal = filtfilt(b,a, signal)

    return filtered_signal