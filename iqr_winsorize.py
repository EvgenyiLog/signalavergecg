import numpy as np
import matplotlib.pyplot as plt

def iqr_winsorize(data:np.ndarray, k:float=1.5)->np.ndarray:
    """
    Робастная фильтрация выбросов методом IQR с заменой на граничные значения (винзоризация).

    Функция вычисляет первый (Q1) и третий (Q3) квартили, интерквартильный размах (IQR),
    а затем определяет нижнюю (lower_bound) и верхнюю (upper_bound) границы "нормальных" значений.
    Все значения, выходящие за эти границы, заменяются на соответствующую границу.

    Параметры:
    ----------
    data : array_like
        Входной одномерный массив данных (может быть список, кортеж или numpy массив).
    k : float, optional
        Коэффициент для определения границ выбросов. Стандартное значение 1.5
        используется для умеренного выявления выбросов. Более высокие значения (например, 3.0)
        делают фильтр менее чувствительным (убирают только экстремальные выбросы).
    include_quartiles : bool, optional
        Если True, функция возвращает не только отфильтрованный массив, но и значения
        Q1, Q3, IQR и границы. По умолчанию False.

    Возвращает:
    -----------
    winsorized_data : numpy.ndarray
        Массив той же формы, что и входной, где выбросы заменены граничными значениями.
    Если include_quartiles=True, возвращает кортеж (winsorized_data, Q1, Q3, IQR, lower_bound, upper_bound)

    Примечания:
    -----------
    Метод IQR устойчив к выбросам, так как использует медиану и квартили,
    которые не чувствительны к экстремальным значениям. Винзоризация (замена)
    предпочтительнее удаления, если важна длина временного ряда или массива.
    """
    # 1. Преобразуем входные данные в numpy массив для удобства
    data = np.asarray(data)

    # Проверка на пустой массив
    if data.size == 0:
        return np.array([])

    # 2. Вычисляем квартили (метод linear используется по умолчанию в numpy 1.22+)
    Q1 = np.percentile(data, 25)
    Q3 = np.percentile(data, 75)

    # 3. Вычисляем интерквартильный размах
    IQR = Q3 - Q1

    # 4. Определяем границы для "не выбросов"
    lower_bound = Q1 - k * IQR
    upper_bound = Q3 + k * IQR

    # 5. Создаем копию данных для модификации
    winsorized_data = data.copy()

    # 6. Заменяем выбросы (Clip / Winsorize)
    # Используем np.clip для ограничения значений снизу и сверху
    # Это самый быстрый и элегантный способ замены.
    winsorized_data = np.clip(winsorized_data, lower_bound, upper_bound)

    # 7. Возвращаем результат
    
    
    return winsorized_data
